#include<iostream>//include library
using namespace std;
int main(){         //write main function
 float hgt,base,area; //declare variables
 cout<<"Enter height of triangle=";
 cin>>hgt;          //Input height of a triangle
 cout<<"Enter base of triangle=";
 cin>>base;        //Input base of a triangle
 area=(hgt*base)/2; //find area of the triangle
 cout<<"The area of the triangle="<<area<<endl;//Display area of the triangle
return 0;
}
